use std::path::{Path, PathBuf};
use std::sync::Arc;
use thiserror::Error;

/// Errors that can occur during collection
#[derive(Debug, Error)]
pub enum CollectorError {
    #[error("Glob pattern error: {0}")]
    GlobPattern(String),

    #[error("IO error: {0}")]
    Io(#[from] io::Error),

    #[error("JSON error: {0}")]
    Json(#[from] serde_json::Error),
}

pub type Result<T> = std::result::Result<T, CollectorError>;

/// Usage record extracted from a Claude Code JSONL assistant message
///
/// Contains token counts for all five token types used in Claude API pricing:
/// - Input tokens: prompt tokens sent to the model
/// - Output tokens: tokens generated by the model
/// - Cache read tokens: cached prompt tokens read
/// - Cache write (5m) tokens: prompt tokens written to 5-minute cache
/// - Cache write (1h) tokens: prompt tokens written to 1-hour cache
#[derive(Debug, Clone, PartialEq)]
pub struct UsageRecord {
    /// Input prompt tokens
    pub input_tokens: u64,

    /// Output generation tokens
    pub output_tokens: u64,

    /// Cached prompt tokens that were read from cache
    pub cache_read_tokens: u64,

    /// Prompt tokens written to 5-minute ephemeral cache
    pub cache_write_5m_tokens: u64,

    /// Prompt tokens written to 1-hour ephemeral cache
    pub cache_write_1h_tokens: u64,

    /// Model identifier (e.g., "claude-sonnet-4-20250514")
    pub model: String,

    /// Session identifier (extracted from JSONL file path)
    pub session: String,
}

impl UsageRecord {
    /// Create a new zero-initialized UsageRecord
    pub fn zero(model: String, session: String) -> Self {
        Self {
            input_tokens: 0,
            output_tokens: 0,
            cache_read_tokens: 0,
            cache_write_5m_tokens: 0,
            cache_write_1h_tokens: 0,
            model,
            session,
        }
    }

    /// Get the total tokens used (all types combined)
    pub fn total_tokens(&self) -> u64 {
        self.input_tokens
            + self.output_tokens
            + self.cache_read_tokens
            + self.cache_write_5m_tokens
            + self.cache_write_1h_tokens
    }

    /// Check if this is a zero record (no tokens used)
    pub fn is_zero(&self) -> bool {
        self.total_tokens() == 0
    }
}

