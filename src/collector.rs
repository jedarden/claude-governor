/// Discover all JSONL files under a base directory
///
/// Walks `base/**/*.jsonl` using glob and returns a sorted list of paths.
pub fn discover_jsonl_files(base: &Path) -> Vec<PathBuf> {
    let pattern = base
        .join("**")
        .join("*.jsonl")
        .to_string_lossy()
        .into_owned();

    let paths = match glob(&pattern) {
        Ok(p) => p,
        Err(e) => {
            log::warn!("Invalid glob pattern '{}': {}", pattern, e);
            return Vec::new();
        }
    };

    let mut files: Vec<PathBuf> = paths.filter_map(|entry| entry.ok()).collect();
    files.sort();
    files
}

/// Persistent store for file read cursors
///
/// Tracks the byte offset for each JSONL file to enable incremental reading.
#[derive(Debug, Clone, Serialize, Deserialize, Default)]
pub struct CursorStore {
    cursors: HashMap<PathBuf, u64>,
}

impl CursorStore {
    /// Load cursor store from a JSON file
    ///
    /// Returns an empty CursorStore if the file doesn't exist.
    pub fn load(path: &Path) -> Result<Self> {
        if !path.exists() {
            return Ok(Self::default());
        }

        let file = fs::File::open(path)?;
        let reader = BufReader::new(file);
        let store: Self = serde_json::from_reader(reader)?;
        Ok(store)
    }

    /// Save cursor store to a JSON file atomically
    ///
    /// Writes to a .tmp file first, then renames to the final path.
    pub fn save(&self, path: &Path) -> Result<()> {
        // Ensure parent directory exists
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)?;
        }

        let tmp_path = path.with_extension("tmp");

        // Write to temp file
        {
            let file = fs::File::create(&tmp_path)?;
            let writer = BufWriter::new(file);
            serde_json::to_writer_pretty(writer, self)?;
        }

        // Atomic rename
        fs::rename(&tmp_path, path)?;

        Ok(())
    }

    /// Get the stored offset for a file
    ///
    /// Returns 0 if the file is not in the store.
    pub fn get_offset(&self, file: &Path) -> u64 {
        self.cursors.get(file).copied().unwrap_or(0)
    }

    /// Set the offset for a file
    pub fn set_offset(&mut self, file: PathBuf, offset: u64) {
        self.cursors.insert(file, offset);
    }
}

/// Extract model identifier from a message JSON object
///
/// Primary: reads `model` field from the message JSON object.
/// Fallback: returns "unknown" if the field is missing or null.
pub fn extract_model(message_json: serde_json::Value) -> String {
    message_json
        .get("model")
        .and_then(|v| v.as_str())
        .map(|s| s.to_string())
        .unwrap_or_else(|| "unknown".to_string())
}

/// Extract session ID from a JSONL file path
///
/// Returns the filename stem (without .jsonl extension).
/// Example: `/home/user/.claude/projects/foo/abc123.jsonl` -> `abc123`
pub fn extract_session_id(jsonl_path: &Path) -> String {
    jsonl_path
        .file_stem()
        .and_then(|stem| stem.to_str())
        .map(|s| s.to_string())
        .unwrap_or_else(|| "unknown".to_string())
}

/// Infer model from path hints
///
/// Checks if the path contains model name hints (e.g., directory with "sonnet" or "opus").
/// Returns None for generic paths without model hints.
pub fn infer_model_from_path(jsonl_path: &Path) -> Option<String> {
    let path_str = jsonl_path.to_string_lossy().to_lowercase();

    // Check for common model identifiers in path
    if path_str.contains("opus") {
        // Could refine to specific opus version if path contains version info
        Some("claude-opus".to_string())
    } else if path_str.contains("sonnet") {
        Some("claude-sonnet".to_string())
    } else if path_str.contains("haiku") {
        Some("claude-haiku".to_string())
    } else {
        None
    }
}

/// Parse a usage block from a JSONL line into a UsageRecord
///
/// Extracts token counts, model, and session from the JSON structure.
pub fn parse_usage_block(line: &JsonlLine, jsonl_path: &Path) -> Option<UsageRecord> {
    // Only process assistant messages with usage data
    let message = line.message.as_ref()?;
    let usage = message.usage.as_ref()?;

    // Extract model: prefer message-level model, fallback to path inference, then "unknown"
    let model = message.model.clone().unwrap_or_else(|| {
        infer_model_from_path(jsonl_path).unwrap_or_else(|| "unknown".to_string())
    });

    // Extract session ID from file path
    let session = extract_session_id(jsonl_path);

    // Extract cache write tokens with 5m/1h breakdown
    let (cache_write_5m, cache_write_1h) = match &usage.cache_creation {
        Some(cc) => (
            cc.ephemeral_5m_input_tokens.unwrap_or(0),
            cc.ephemeral_1h_input_tokens.unwrap_or(0),
        ),
        None => {
            // Legacy format: cache_creation_input_tokens without breakdown
            // Treat all as 5m (conservative assumption)
            (usage.cache_creation_input_tokens.unwrap_or(0), 0)
        }
    };

    Some(UsageRecord {
        input_tokens: usage.input_tokens,
        output_tokens: usage.output_tokens,
        cache_read_tokens: usage.cache_read_input_tokens.unwrap_or(0),
        cache_write_5m_tokens: cache_write_5m,
        cache_write_1h_tokens: cache_write_1h,
        model,
        session,
    })
}

/// Read new lines from a JSONL file since the last cursor position
///
/// Seeks to the stored byte offset and reads all content to EOF.
/// If the file has shrunk (log rotation), resets the cursor to 0 and logs a warning.
pub fn read_new_lines(path: &Path, cursor: &mut CursorStore) -> Result<Vec<String>> {
    let stored_offset = cursor.get_offset(path);
    let mut file = fs::File::open(path)?;
    let file_size = file.metadata()?.len();

    let start_offset = if file_size < stored_offset {
        log::warn!(
            "[collector] cursor reset for {} (file shrunk: {} -> {} bytes)",
            path.display(),
            stored_offset,
            file_size
        );
        0
    } else {
        stored_offset
    };

    if start_offset > 0 {
        file.seek(SeekFrom::Start(start_offset))?;
    }

    let mut content = String::new();
    file.read_to_string(&mut content)?;

    let lines: Vec<String> = content
        .split('\n')
        .filter(|line| !line.is_empty())
        .map(|line| line.to_string())
        .collect();

    let new_offset = start_offset + content.len() as u64;
    cursor.set_offset(path.to_path_buf(), new_offset);

    Ok(lines)
}

/// Forecast record for a single usage window
///
/// Captures the current snapshot, ceiling, and projected exhaustion metrics
/// for one of the three Claude Code usage windows (5h, 7d, 7d-sonnet).
#[derive(Debug, Clone, Serialize)]
pub struct WindowRecord {
    /// Record type discriminator (always "w" for window)
    #[serde(rename = "r")]
    pub r: String,

    /// Timestamp of this record
    #[serde(rename = "ts")]
    pub ts: DateTime<Utc>,

    /// Window name: "five_hour", "seven_day", "seven_day_sonnet"
    #[serde(rename = "win")]
    pub win: String,

    /// Peak flag
    #[serde(rename = "pk")]
    pub pk: bool,

    /// Target ceiling from config (e.g. 90.0)
    #[serde(rename = "ceil")]
    pub ceil: f64,

    /// Raw platform utilization %
    #[serde(rename = "snap")]
    pub snap: f64,

    /// Window reset time
    #[serde(rename = "reset")]
    pub reset: DateTime<Utc>,

    /// Pct change this interval (set externally when comparing to previous snapshot)
    #[serde(rename = "delta")]
    pub delta: f64,

    /// Remaining budget: ceil - snap (NOT 100 - snap)
    #[serde(rename = "remain")]
    pub remain: f64,

    /// Hours until window resets
    #[serde(rename = "hrs_left")]
    pub hrs_left: f64,

    /// Fleet burn rate as pct/hr
    #[serde(rename = "fleet_pct_hr")]
    pub fleet_pct_hr: f64,

    /// Hours until exhaustion: remain / fleet_pct_hr
    #[serde(rename = "exh_hrs")]
    pub exh_hrs: f64,

    /// 1 if exh_hrs < hrs_left (will exhaust before window resets)
    #[serde(rename = "cutoff_risk")]
    pub cutoff_risk: u8,

    /// Margin hours: hrs_left - exh_hrs (can be negative)
    #[serde(rename = "margin_hrs")]
    pub margin_hrs: f64,

    /// 1 if this is the most constrained (binding) window
    #[serde(rename = "bind")]
    pub bind: u8,

    /// Safe window in hours (only set on binding window)
    #[serde(rename = "safe_w", skip_serializing_if = "Option::is_none")]
    pub safe_w: Option<u32>,
}

/// Compute a window forecast from current snapshot data
///
/// Derives all forecast fields (remain, hrs_left, exh_hrs, cutoff_risk, margin_hrs)
/// from the raw inputs. The `bind` and `safe_w` fields are left at defaults (0/None)
/// and are set later by `find_binding_window`.
pub fn compute_window_forecast(
    win: &str,
    snap: f64,
    ceil: f64,
    reset: DateTime<Utc>,
    now: DateTime<Utc>,
    fleet_pct_hr: f64,
    pk: bool,
) -> WindowRecord {
    let remain = ceil - snap;
    let hrs_left = (reset - now).num_seconds() as f64 / 3600.0;
    let exh_hrs = if fleet_pct_hr > 0.0 {
        remain / fleet_pct_hr
    } else {
        f64::INFINITY
    };
    let cutoff_risk = if exh_hrs < hrs_left { 1 } else { 0 };
    let margin_hrs = hrs_left - exh_hrs;

    WindowRecord {
        r: "w".to_string(),
        ts: now,
        win: win.to_string(),
        pk,
        ceil,
        snap,
        reset,
        delta: 0.0,
        remain,
        hrs_left,
        fleet_pct_hr,
        exh_hrs,
        cutoff_risk,
        margin_hrs,
        bind: 0,
        safe_w: None,
    }
}

/// Find the binding (most constrained) window and set its `bind = 1`.
///
/// The binding window is the one with the smallest (most negative) `margin_hrs`.
/// All other windows get `bind = 0`. The binding window also gets `safe_w`
/// computed based on the provided per-worker rate.
pub fn find_binding_window(
    forecasts: &mut [WindowRecord],
    p75_rate_per_worker: f64,
) {
    if forecasts.is_empty() {
        return;
    }

    // Find the index of the window with the smallest margin_hrs
    let binding_idx = forecasts
        .iter()
        .enumerate()
        .min_by(|(_, a), (_, b)| {
            a.margin_hrs
                .partial_cmp(&b.margin_hrs)
                .unwrap_or(std::cmp::Ordering::Equal)
        })
        .map(|(i, _)| i)
        .unwrap_or(0);

    for (i, rec) in forecasts.iter_mut().enumerate() {
        if i == binding_idx {
            rec.bind = 1;
            rec.safe_w = Some(compute_safe_workers(
                rec.remain,
                rec.hrs_left,
                p75_rate_per_worker,
            ));
        } else {
            rec.bind = 0;
            rec.safe_w = None;
        }
    }
}

/// Compute the maximum number of workers where exhaustion time >= window time.
///
/// `safe_w = floor(remaining_pct / (p75_rate_per_worker * hrs_left))`
/// Returns `u32::MAX` when there's no constraint (zero rate or zero hours).
pub fn compute_safe_workers(remaining_pct: f64, hrs_left: f64, p75_rate_per_worker: f64) -> u32 {
    if p75_rate_per_worker <= 0.0 || hrs_left <= 0.0 {
        return u32::MAX;
    }

    let safe = (remaining_pct / (p75_rate_per_worker * hrs_left)).floor() as u64;
    safe.min(u32::MAX as u64) as u32
}

/// Generate WindowRecords for all three usage windows and find the binding one.
///
/// Produces exactly 3 records: five_hour, seven_day, seven_day_sonnet.
pub fn compute_window_forecasts(
    windows: &[(&str, f64, DateTime<Utc>)], // (name, snap, reset)
    ceil: f64,
    now: DateTime<Utc>,
    fleet_pct_hr: f64,
    pk: bool,
    p75_rate_per_worker: f64,
) -> Vec<WindowRecord> {
    let mut forecasts: Vec<WindowRecord> = windows
        .iter()
        .map(|(name, snap, reset)| {
            compute_window_forecast(name, *snap, ceil, *reset, now, fleet_pct_hr, pk)
        })
        .collect();

    find_binding_window(&mut forecasts, p75_rate_per_worker);
    forecasts
}

